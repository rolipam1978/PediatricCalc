    private void tfPesoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfPesoFocusLost
        
        if(tfPeso.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Digite o peso do paciente para continuar.");
            tfPeso.requestFocus();
        }
        
        peso = Double.parseDouble(tfPeso.getText().replace(",", "."));
        th = Double.parseDouble(tfTH.getText().replace(",", "."));
        
        // Regra de Holliday-Segar
        if(peso <= 10.0){
            tfRegra.setText("Até 10Kg - 100mL / Kg / Dia");
            volume = (100 * peso * th)/100;
              
        }else if(peso > 10.0 && peso <= 20.0){
            tfRegra.setText("1.000mL + 50mL / Kg / Dia - para cada Kg acima de 10Kg");
            volume = (1000 + (50 * (peso - 10))) * th/100;
            
        }else if(peso > 20.0 && peso < 30.0){
            tfRegra.setText("1.500mL + 20mL / Kg / Dia - para cada Kg acima de 20Kg");
            volume = (1500 + (20 * (peso - 20))) * th/100;
            
        }else{
            // peso >= 30Kg
            tfRegra.setText("Acima de 30Kg - 60mL / Kg / Dia");
            volume = (60 * peso * th)/100;
         
        }
        tfVolume.setText(df.format(volume));
        vazao = volume/24;
        tfVazao.setText(df.format(vazao));
        
        // Nutrição Parenteral de Cálcio (Manutenção)
        
        if(peso <= 50.0){
            tfObsCalcio.setText("Bebês e Crianças até 50Kg: 0,5 (10,3mg) a 4,0mEq (82,7mg)/Kg/dia. Dose máxima: 1000mg (48,4mEq)/dose.");
        }else{
            tfObsCalcio.setText("Crianças acima de 50Kg: 10 (206,7mg) a 20mEq (413,3mg)/dia. Dose máxima: 2000 (96,8mEq) a 3000mg (145,2mEq)/dose.");
        }
        tfTIG.requestFocus();
    }//GEN-LAST:event_tfPesoFocusLost

