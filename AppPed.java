    private void tfDose1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfDose1FocusLost
        if(tfDose1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Necessário preencher esse campo!");
            tfDose1.requestFocus();
        }else{
            concentracao = Double.parseDouble(tfCC1.getText().replace(",", "."));
            dosePrescrita = Double.parseDouble(tfDose1.getText().replace(",", "."));
            volumeMedicamento = dosePrescrita/concentracao;
            tfVolMed1.setText(df.format(volumeMedicamento));
            volumeTotal = volumeMedicamento + volumeEquipo;
            tfVolTT1.setText(df.format(volumeTotal));
            tfVolTT2.setText(df.format(volumeTotal));
            tfVolTT4.setText(df.format(volumeTotal));
            doseAjustada = concentracao * volumeTotal;
            tfDoseAjustada2.setText(df.format(doseAjustada));
            tfDoseAjustada3.setText(df.format(doseAjustada));
            doseApresentacao = Double.parseDouble(tfDoseGeral.getText().replace(",", "."));
            volumeReconstituicao = Double.parseDouble(tfVolExpGeral.getText().replace(",", "."));
            volumeAspirar = (doseAjustada * volumeReconstituicao)/doseApresentacao;
            tfVolAspirar3.setText(df.format(volumeAspirar));
            tfVolAspirar4.setText(df.format(volumeAspirar));
            volumeSoro = volumeTotal - volumeAspirar;
            tfVolSoro4.setText(df.format(volumeSoro));
        }
    }//GEN-LAST:event_tfDose1FocusLost

